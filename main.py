# Main menu function
def make_a_choice():
    print("\nChoose an option:")
    print("1. Output a circle")
    print("2. Output a set of lines")
    print("3. Output a random design")
    print("4. Exit")

    try:
        choice = int(input("Enter your choice (1, 2, 3, or 4): "))
        return choice
    except ValueError:
        print("Invalid input. Please enter a number between 1 and 4.")
        return None


def main():
    choice = make_a_choice()  # Get the user's choice before entering the loop
    SENTINEL = 4

    while choice != SENTINEL:
        if choice == 1:
            draw_circle()
        elif choice == 2:
            draw_lines()
        elif choice == 3:
            random_design()
        elif choice == SENTINEL:
            break  # This is to exit the loop
        else:
            print("Invalid choice. Please enter 1, 2, 3, or 4.")

        # Ask for the user's choice again
        choice = make_a_choice()

    print("Hope you enjoyed!")


# Run the menu function to start the program
main()
