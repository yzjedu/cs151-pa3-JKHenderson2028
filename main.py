import random


# Function to draw a circle
def draw_circle():
    circle_art = """
       ____  
     /      \ 
    |        | 
     \ ____ /  
    """
    print(circle_art)


# Function to draw a set of lines based on user input
def draw_lines():
    try:
        # Input for the number of lines
        num_lines = int(input("How many lines would you like to draw? "))

        # Input for the character(s) to repeat
        char = input("What character(s) would you like to repeat? ")

        # Input for the number of times the character(s) should be repeated
        repeat_count = int(input("How many times would you like to repeat the character(s)? "))

        # Print the lines
        for i in range(num_lines):
            print(char * repeat_count)

    except ValueError:
        print("Invalid input. Please enter valid numbers for the line count and repetition.")


